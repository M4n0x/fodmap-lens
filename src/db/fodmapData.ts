export async function seedFodmapData(db: SQLiteDatabase): Promise<void> {
  const now = new Date().toISOString();

  for (const item of SEED_DATA) {
    const result = await db.runAsync(
      `INSERT OR IGNORE INTO fodmap_ingredients (canonical_key, category, fructans, gos, lactose, excess_fructose, sorbitol, mannitol, overall_rating, notes, notes_fr, notes_de, source, confidence, updated_at)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 'literature', 1.0, ?)`,
      [
        item.canonical_key,
        item.category,
        item.fructans,
        item.gos,
        item.lactose,
        item.excess_fructose,
        item.sorbitol,
        item.mannitol,
        item.overall_rating,
        item.notes,
        item.notes_fr,
        item.notes_de,
        now,
      ]
    );

    // If row was ignored (duplicate canonical_key), look up existing id
    let ingredientId = result.lastInsertRowId;
    if (result.changes === 0) {
      const existing = await db.getFirstAsync<{ id: number }>(
        'SELECT id FROM fodmap_ingredients WHERE canonical_key = ?',
        [item.canonical_key]
      );
      if (!existing) continue;
      ingredientId = existing.id;
    }

    for (const [lang, synonyms] of Object.entries(item.synonyms)) {
      for (let i = 0; i < synonyms.length; i++) {
        await db.runAsync(
          `INSERT OR IGNORE INTO ingredient_synonyms (fodmap_ingredient_id, synonym, language, is_primary) VALUES (?, ?, ?, ?)`,
          [ingredientId, synonyms[i].toLowerCase(), lang, i === 0 ? 1 : 0]
        );
      }
    }
  }
}
